public class Compte {
    private Integer c_id;
    private String c_civilite;
    private String c_nom;
    private String c_prenom;
    private String c_tel;
    private String c_mail;
    private String c_adresse;
    private String c_mdp;

    public Compte() {
    }

    public Integer getC_id() {
        return c_id;
    }

    public void setC_id(Integer c_id) {
        this.c_id = c_id;
    }

    public String getC_civilite() {
        return c_civilite;
    }

    public void setC_civilite(String c_civilite) {
        this.c_civilite = c_civilite;
    }

    public String getC_nom() {
        return c_nom;
    }

    public void setC_nom(String c_nom) {
        this.c_nom = c_nom;
    }

    public String getC_prenom() {
        return c_prenom;
    }

    public void setC_prenom(String c_prenom) {
        this.c_prenom = c_prenom;
    }

    public String getC_tel() {
        return c_tel;
    }

    public void setC_tel(String c_tel) {
        this.c_tel = c_tel;
    }

    public String getC_mail() {
        return c_mail;
    }

    public void setC_mail(String c_mail) {
        this.c_mail = c_mail;
    }

    public String getC_adresse() {
        return c_adresse;
    }

    public void setC_adresse(String c_adresse) {
        this.c_adresse = c_adresse;
    }

    public String getC_mdp() {
        return c_mdp;
    }

    public void setC_mdp(String c_mdp) {
        this.c_mdp = c_mdp;
    }
}
